#!/usr/bin/env bash

set -euo pipefail

SCRIPT_NAME="$(basename "$0")"

usage() {
  cat <<EOF
AgentOS Package Installer

Usage:
  $SCRIPT_NAME install --package <path> --target <dir> [options]

Options:
  -p, --package     Package path (directory with rules/skills/workflows)
  -t, --target      Target project directory
  -f, --format      Target format:
                    kilocode | antigravity | cursor |
                    agents | claude | codex | opencode |
                    both | all (default: all)
  -d, --dry-run     Show what would be copied
  -h, --help        Show this help

Examples:
  $SCRIPT_NAME install -p agentos/modules/backend -t ./my-project -f all
  $SCRIPT_NAME install -p agentos/modules/backend -t ./my-project -f codex
  $SCRIPT_NAME install -p agentos/modules/backend -t . -f kilocode --dry-run
EOF
}

log() {
  echo "[agentos] $1"
}

copy_dir() {
  local src="$1"
  local dest="$2"

  if [ "$DRY_RUN" = true ]; then
    log "DRY-RUN: cp -a $src -> $dest/"
  else
    cp -a "$src" "$dest/"
  fi
}

copy_tree_targets() {
  local target_path="$1"
  local src_dir

  if [ "$DRY_RUN" = false ]; then
    mkdir -p "$target_path"
  fi

  for src_dir in "${SOURCE_DIRS[@]}"; do
    local src_path="$PACKAGE_PATH/$src_dir"

    if [ -d "$src_path" ]; then
      log "Copying $src_dir → $target_path"
      copy_dir "$src_path" "$target_path"
    else
      log "Skipping $src_dir (not found)"
    fi
  done
}

render_agents_md() {
  local out_file="$1"
  local package_name
  package_name="$(basename "$PACKAGE_PATH")"

  if [ -f "$PACKAGE_PATH/AGENTS.md" ]; then
    if [ "$DRY_RUN" = true ]; then
      log "DRY-RUN: cp -a $PACKAGE_PATH/AGENTS.md -> $out_file"
    else
      cp -a "$PACKAGE_PATH/AGENTS.md" "$out_file"
    fi
    return
  fi

  if [ -f "$PACKAGE_PATH/templates/AGENTS.md" ]; then
    if [ "$DRY_RUN" = true ]; then
      log "DRY-RUN: cp -a $PACKAGE_PATH/templates/AGENTS.md -> $out_file"
    else
      cp -a "$PACKAGE_PATH/templates/AGENTS.md" "$out_file"
    fi
    return
  fi

  log "No AGENTS.md template found. Building a fallback AGENTS.md from package folders."

  if [ "$DRY_RUN" = true ]; then
    log "DRY-RUN: generate fallback $out_file"
    return
  fi

  {
    echo "# AGENTS.md"
    echo
    echo "This file was generated by agentos-install.sh from package: $package_name."
    echo
    echo "## Rules"
    [ -d "$PACKAGE_PATH/rules" ] && find "$PACKAGE_PATH/rules" -maxdepth 1 -type f -name '*.md' -printf '- %f\n' | sort || echo "- (none)"
    echo
    echo "## Skills"
    [ -d "$PACKAGE_PATH/skills" ] && find "$PACKAGE_PATH/skills" -maxdepth 1 -type f -name '*.md' -printf '- %f\n' | sort || echo "- (none)"
    echo
    echo "## Workflows"
    [ -d "$PACKAGE_PATH/workflows" ] && find "$PACKAGE_PATH/workflows" -maxdepth 1 -type f -name '*.md' -printf '- %f\n' | sort || echo "- (none)"
    echo
    echo "## Prompts"
    [ -d "$PACKAGE_PATH/prompts" ] && find "$PACKAGE_PATH/prompts" -maxdepth 1 -type f -name '*.md' -printf '- %f\n' | sort || echo "- (none)"
  } > "$out_file"
}

# Defaults
FORMAT="all"
DRY_RUN=false

if [ $# -eq 0 ]; then
  usage
  exit 1
fi

COMMAND="$1"
shift

if [ "$COMMAND" != "install" ]; then
  usage
  exit 1
fi

# Parse args
while [[ $# -gt 0 ]]; do
  case "$1" in
    -p|--package)
      PACKAGE_PATH="$2"
      shift 2
      ;;
    -t|--target)
      TARGET_ROOT="$2"
      shift 2
      ;;
    -f|--format)
      FORMAT="$2"
      shift 2
      ;;
    -d|--dry-run)
      DRY_RUN=true
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "Unknown option: $1"
      usage
      exit 1
      ;;
  esac
done

if [ -z "${PACKAGE_PATH:-}" ] || [ -z "${TARGET_ROOT:-}" ]; then
  echo "Error: --package and --target are required."
  usage
  exit 1
fi

if [ ! -d "$PACKAGE_PATH" ]; then
  echo "Error: package directory not found: $PACKAGE_PATH"
  exit 1
fi

SOURCE_DIRS=(rules skills workflows prompts)

case "$FORMAT" in
  antigravity)
    TARGET_SUB_DIRS=(.agent)
    ;;
  kilocode)
    TARGET_SUB_DIRS=(.kilocode)
    ;;
  cursor)
    TARGET_SUB_DIRS=(.cursor)
    ;;
  agents|claude|codex|opencode)
    TARGET_SUB_DIRS=()
    INSTALL_AGENTS_MD=true
    ;;
  both)
    TARGET_SUB_DIRS=(.agent .kilocode)
    ;;
  all)
    TARGET_SUB_DIRS=(.agent .kilocode .cursor)
    INSTALL_AGENTS_MD=true
    ;;
  *)
    echo "Invalid format: $FORMAT"
    exit 1
    ;;
esac

log "Installing package: $PACKAGE_PATH"
log "Target project: $TARGET_ROOT"
log "Format: $FORMAT"
log "Dry-run: $DRY_RUN"

for target in "${TARGET_SUB_DIRS[@]:-}"; do
  [ -z "$target" ] && continue
  copy_tree_targets "$TARGET_ROOT/$target"
done

if [ "${INSTALL_AGENTS_MD:-false}" = true ]; then
  log "Rendering AGENTS.md → $TARGET_ROOT/AGENTS.md"
  render_agents_md "$TARGET_ROOT/AGENTS.md"
fi

log "Done."
